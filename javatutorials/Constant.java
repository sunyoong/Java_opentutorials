package javatutorials;

public class Constant {
	public static void main(String[] args) {
		/* 
		 * int type
		 * 1 => 사과
		 * 2 => 복숭아
		 * 3 => 바나나
		 */
		int type = 1; // 이렇게만 해놓으면 1이 뭔지 코드만 보고는 모름. 그래서 필요한게 상수.
		switch(type){
			case 1 :
				System.out.println(57);
				break;
			case 2 :
				System.out.println(34);
				break;
			case 3 : 
				System.out.println(93);
				break;
		}
		
		
	}
}
